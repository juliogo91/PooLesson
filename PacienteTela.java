    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Paciente pessoa = new Paciente();
        
        pessoa.setNomeCompleto(jTextField1CampoNome.getText());
        pessoa.setPeso(Integer.parseInt(jTextField2CampoPeso.getText()));
        pessoa.setAltura(Float.parseFloat(jTextField3CampoAltura.getText()));
        pessoa.setSexo((String)jComboBox1Sexo.getSelectedItem());
        
        String saida = "CL√çNICA GYN \n";
        saida = saida + "DADOS DO PACIENTE: \n";
        saida += "NOME COMPLETO: " + pessoa.getNomeCompleto() + "\n";
        saida += "PESO: " + pessoa.getPeso() + "\n";
        saida += "ALTURA: " + pessoa.getAltura() + "\n";
        saida += "SEXO: " + pessoa.getAltura() + "\n";
        saida += "IMC: " + pessoa.calcularIMC() + "\n";
        
        jTextArea1CampoArea.setText(saida);
        jTextField1CampoNome.setText(" ");
        jTextField2CampoPeso.setText(" ");
        jTextField3CampoAltura.setText(" ");
    }//GEN-LAST:event_jButton1ActionPerformed

